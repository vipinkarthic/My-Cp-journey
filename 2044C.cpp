void solve() {
    ll m, a, b, c; cin >> m >> a >> b >> c;
    ll row1 = min(a, m);
    ll row2 = min(b, m);
    ll remaining_seats = (2 * m) - (row1 + row2);
    ll no_pref = min(remaining_seats, c);
    cout << row1 + row2 + no_pref;
}

